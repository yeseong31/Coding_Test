# 상하좌우
# 여행가 A는 N X N 크기의 정사각형 공간 위에 서 있다. 이 공간은 1 X 1 크기의 정사각형으로 나누어져 있다.
# 가장 왼쪽 위 좌표는 (1, 1)이며, 가장 오른쪽 아래 좌효는 (N, N)에 해당한다. 여행가 A는 상, 하, 좌, 우 방향으로
# 이동할 수 있으며, 시작 좌표는 항상 (1, 1)이다. 계획서에는 하나의 줄에 띄어쓰기를 기준으로 하여 L, R, U, D 중
# 하나의 문자가 반복적으로 적혀 있다. 각각 왼쪽, 오른쪽, 위쪽, 아래쪽으로 한 칸 이동을 의미한다. 이때 여행가 A가
# N X N 크기의 정사각형 공간을 벗어나는 움직임은 무시된다. 계획서가 주어졌을 때 여행가 A가 최종적으로 도착할 지점의
# 좌표를 출력하는 프로그램을 작성하시오.

# 입력 조건
# - 첫째 줄이 공간의 크기를 나타내는 N이 주어진다. (1 <= N <= 100)
# - 둘째 줄에 여행가 A가 이동할 계획서 내용이 주어진다. (1 <= 이동 횟수 <= 100)
# 출력 조건
# - 첫째 줄에 여행가 A가 최종적으로 도착할 지점의 좌표(X, Y)를 공백으로 구분하여 출력한다.

# 입력 예시
# 5
# R R R U D D
# 출력 예시
# 3 4

# -----------------------------------------------------------------------------------------------
n = int(input())
plans = input().split()
x, y = 1, 1     # 시작 좌표

# L, R, U, D에 따른 이동 방향
dx = [0, 0, -1, 1]
dy = [-1, 1, 0, 0]
move_types = ['L', 'R', 'U', 'D']

# 이동 계획을 하나씩 확인
for plan in plans:
    # 이동 후 좌표 구하기
    for i in range(len(move_types)):
        if plan == move_types[i]:
            nx = x + dx[i]
            ny = y + dy[i]
    # 공간을 벗어나는 경우 무시
    if nx < 1 or nx > n or ny < 1 or ny > n:
        continue
    # 이동 수행
    x, y = nx, ny

print(x, y)

# -----------------------------------------------------------------------------------------------
# 연산 횟수는 이동 횟수에 비례하므로 시간 복잡도는 O(N)이다.
# 일련의 명령에 따라 개체를 차례대로 이동시킨다는 점에서 시뮬레이션 유형으로 분류되며
# 구현이 중요한 대표적인 문제 유형이다.
# 코딩 테스트에서의 시뮬레이션 유형, 구현 유형, 완전 탐색 유형은 서로 유사한 점이 많다는 정도로만 기억하자.
